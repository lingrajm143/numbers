# Grocery store inventory management

# Step 1: Create the initial inventory
inventory = ["Apples", "Bananas", "Carrots", "Dates"]

# Step 2: Print the current inventory
print("Current inventory:", inventory)

# Step 3: Add "Eggs" to the inventory
inventory.append("Eggs")
print("After adding Eggs:", inventory)

# Step 4: Remove "Bananas" from the inventory
inventory.remove("Bananas")
print("After removing Bananas:", inventory)

# Step 5: Sort the inventory alphabetically
inventory.sort()
print("Final sorted inventory:", inventory)